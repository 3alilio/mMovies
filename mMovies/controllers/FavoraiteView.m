#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return _myFavMovies.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"FavoraiteCell" forIndexPath:indexPath];
    
    UIImageView *favMovieImage  = [cell viewWithTag:1];
    Movie  *m = [_myFavMovies objectAtIndex:indexPath.row];
    
    NSString *imageUrl= [NSString stringWithFormat:@"https://image.tmdb.org/t/p/w185//%@",m.posterPath];
    NSLog(@"\n%@\n",imageUrl);
    
    [favMovieImage sd_setImageWithURL:[NSURL URLWithString:imageUrl]
                   placeholderImage:[UIImage imageNamed:@"help.png"]];
    
    return cell;
}


